# Booleans - Represents one of two values: True or False.
is_true = True
is_false = False
print("is_true:", is_true)
print("is_false:", is_false)




# We can use them in conditional statements
if True:
  print("Block IF will always execute because the condition is True.")
else:
  print("Block ELSE will never execute because the condition is True.")


# AND, OR, and NOT operators
# AND operator: Returns True if both operands are True, otherwise returns False.
print("True AND True:", True and True)  # True
print("True AND False:", True and False)  # False
print("False AND True:", False and True)  # False
print("False AND False:", False and False)  # False 

