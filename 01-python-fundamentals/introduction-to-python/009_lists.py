# Lists - Ordered, mutable collection of items.


my_list = [1, 2, 3, 4, 5, "Engineering", True, False]


# Printing the entire list
print("My List is: ", my_list)

# Accessing elements in a list using indexing
print("First element of the list: ", my_list[0])


# Slicing a list
print("Sliced list (from index 1 to 4):", my_list[1:5]) # Slicing from index 1 to 4 (5 is exclusive)
print("Sliced list (from index 2 to end):", my_list[2:]) # Slicing from index 2 to the end