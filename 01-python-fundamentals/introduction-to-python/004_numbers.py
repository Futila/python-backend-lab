#Numbers

# Integers
# Integers are whole numbers, positive or negative, without decimals, of unlimited length.

number_integer = 10
print("Integer:", number_integer)


# Float
# Float, or floating point numbers, are numbers with a decimal point.

number_float = 10.5
print("Float:", number_float)



# type () 
# The type() function is used to determine the type of a variable.

print("Type of number_integer:", type(number_integer))
print("Type of number_float:", type(number_float))